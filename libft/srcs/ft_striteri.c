#include "libft.h"

void ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	char *str;
	size_t i;

	i = 0;
	if (!s)
		return (NULL);
	str = malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!str)
		return (NULL);
	while (*(s + i))
	{
		(*f)(i, (s + i));
		i++;
	}
	*(str + i) = '\0';
}
